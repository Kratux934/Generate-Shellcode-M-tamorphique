def count_differences(shellcode1, shellcode2):
    min_length = min(len(shellcode1), len(shellcode2))
    differences = abs(len(shellcode1) - len(shellcode2))
    
    for i in range(min_length):
        if shellcode1[i] != shellcode2[i]:
            differences += 1
    
    return differences

